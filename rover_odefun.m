function dx = rover_odefun(x, u, p)
    
    dx = zeros(3,1);
    m = p(1);
    a = p(2);
    b = p(3);
    Cx = p(4);
    Cy = p(5);
    CA = p(6);
    
    dx(1) = x(2)*x(3)+1/m(1)*(Cx(1)*(u(1)+u(2))*cos(u(5)) ...
              -2*Cy(1)*(u(5)-(x(2)+a(1)*x(3))/x(1))*sin(u(5)) ...
              +Cx(1)*(u(3)+u(4))-CA(1)*x(1)^2);
    dx(2) = -x(1)*x(3)+1/m(1)*(Cx(1)*(u(1)+u(2))*sin(u(5)) ...
              +2*Cy(1)*(u(5)-(x(2)+a(1)*x(3))/x(1))*cos(u(5)) ...
              +2*Cy(1)*(b(1)*x(3)-x(2))/x(1));
    dx(3) = 1/(((a(1)+b(1))/2)^2*m(1)) ...
              *(a(1)*(Cx(1)*(u(1)+u(2))*sin(u(5)) ...
              +2*Cy(1)*(u(5)-(x(2)+a(1)*x(3))/x(1))*cos(u(5))) ...
              -2*b(1)*Cy(1)*(b(1)*x(3)-x(2))/x(1));
                   
end


 